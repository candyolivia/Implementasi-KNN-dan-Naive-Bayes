    private void determineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_determineActionPerformed
        System.out.println("algorithm: " + algorithmBox.getSelectedIndex());
        System.out.println("buying: " + buyingBox.getSelectedIndex());
        System.out.println("maint: " + maintBox.getSelectedIndex());
        System.out.println("doors: " + doorsBox.getSelectedIndex());
        System.out.println("persons: " + personsBox.getSelectedIndex());
        System.out.println("lug boot: " + lugbootBox.getSelectedIndex());
        System.out.println("safety: " + safetyBox.getSelectedIndex());
        String[] attrClass = {"unacc","acc","good","vgood"};
        String[] attBuying = { "buying","vhigh","high","med","low" };
        String[] attMaint = { "maint","vhigh","high","med","low" };
        String[] attDoors = { "doors","2","3","4","5more" };
        String[] attPersons = { "persons","2","4","more" };
        String[] attLugboot = { "lugboot","small","med","big" };
        String[] attSafety = { "safety","high","med","low" };
        String[] arrAtt = {attBuying[buyingBox.getSelectedIndex()],attMaint[maintBox.getSelectedIndex()],
            attDoors[doorsBox.getSelectedIndex()], attPersons[personsBox.getSelectedIndex()],
            attLugboot[lugbootBox.getSelectedIndex()], attSafety[safetyBox.getSelectedIndex()] };
        List<String> attInput = new ArrayList<>(Arrays.asList(arrAtt));
        areaResult.setText("Kelas dengan atribute:\n");
        areaResult.append("buying: " + buyingBox.getSelectedIndex()+"\n");
        areaResult.append("maint: " + maintBox.getSelectedIndex()+"\n");
        areaResult.append("doors: " + doorsBox.getSelectedIndex()+"\n");
        areaResult.append("persons: " + personsBox.getSelectedIndex()+"\n");
        areaResult.append("lug boot: " + lugbootBox.getSelectedIndex()+"\n");
        areaResult.append("safety: " + safetyBox.getSelectedIndex()+"\n");
        areaResult.append("File name: " + filename +"\n");
        if(algorithmBox.getSelectedIndex()==1) { //NAIVE BAYES
            NaiveBayes nb = new NaiveBayes(filename);
            areaResult.append("Kelas: " + nb.classPrediction(attInput));
        }
        else if(algorithmBox.getSelectedIndex()==2) { //KNN
            knn.Process knn = new knn.Process(filename);
            int[] arrIntAtt = {buyingBox.getSelectedIndex()-1,maintBox.getSelectedIndex()-1,
                    doorsBox.getSelectedIndex()-1, personsBox.getSelectedIndex()-1,
                    lugbootBox.getSelectedIndex()-1, safetyBox.getSelectedIndex()-1};
            knn.setAnalyseClass(arrIntAtt);
            areaResult.append("Kelas: " + attrClass[knn.determineClassCarData()]);
            
        }
    }//GEN-LAST:event_determineActionPerformed

